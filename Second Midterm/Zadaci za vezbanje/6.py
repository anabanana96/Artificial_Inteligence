def calculate_percision(predictions, true_classes):
    tp_count = 0
    fp_count = 0
    for x,y in zip(predictions, true_classes):
        if x == 1 and y == 1: tp_count += 1
        if x == 1 and y == 0: fp_count += 1

    if((tp_count + fp_count) == 0): return 0.0
    return tp_count / (tp_count + fp_count)

def split_set(set):
    return [row[:-1] for row in set], [row[-1] for row in set]
def divide_sets(X, dataset):
    x = len(dataset) - X
    count = 0
    train_set, test_set = [],[]
    for row in dataset:
        count += 1
        if count <= x: train_set.append(row)
        else: test_set.append(row)

    return train_set, test_set


